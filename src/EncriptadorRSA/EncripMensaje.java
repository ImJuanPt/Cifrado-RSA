    //metodo para comprobar si el numero que se le da como parametro es primo.
    public boolean nprimo(int n) {
        int div = 0;
        for (int i = 1; i <= n; i++) {
            if (n % i == 0) {
                div++;
            }
        }
        return div <= 2;
    }
    
    public void ComprobarInverso() {
        mod = (Integer.parseInt(txtQ.getText())  - 1) * (Integer.parseInt(txtP.getText()) - 1);
        int answ;

        for (int a = 1; a < mod; a++) {
            for (int i = 1; i < mod; i++) {
                
                answ = (a * i) % mod;
                if (answ == 1) {
                    jCLambda.addItem(String.valueOf(a));
                }
            }
        }

    }

