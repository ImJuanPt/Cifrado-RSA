    //metodo para el proceso de elevar el numero que recibe como parametro al lambda, 
    //la funcion es reducir la potencia(en este caso el lambda) para que esta no sea muy elevada y rompa el programa
    public int generarInversoElevado(int numero) {
        int limite, respuesta, aux;
        if (numLambda >= 5) {
            limite = numLambda / 2;
            respuesta = (int) Math.pow(numero, 2);
            aux = (respuesta % n); 
            respuesta = aux;
            for (int i = 0; i < (limite) - 2; i++) { 
                respuesta = (respuesta * aux) % n;
            }
            respuesta = (respuesta * ((aux * numero) % n)) % n;
        } else {
            return (int) ((Math.pow(numero, numLambda)) % n);
        }
        return (int) respuesta;
    }

